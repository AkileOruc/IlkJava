package day11_forLoop;

public class C09_NestedLoopSekil {
    public static void main(String[] args) {


       int kisaKenar= 6;
       int uzunKenar=9;

        for (int i = 1; i <=kisaKenar; i++) {

            for (int j = 1; j <=uzunKenar ; j++) {
                System.out.print("* ");

            }
            System.out.println("");
        }






    }





}
